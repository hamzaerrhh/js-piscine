 const more =function(arg) {
    n = parseFloat(arg)
    return ++n
}

const less =function (arg){
    n= parseFloat(arg)
    return --n
}

const add = function (arg1,arg2){
    n1= parseFloat(arg1)
    n2= parseFloat(arg2)
    return n1+n2
}

const sub =function (arg1,arg2){
    n1= parseFloat(arg1)
    n2= parseFloat(arg2)
    return n1-n2
}
